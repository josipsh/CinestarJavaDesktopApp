    private void tfSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchKeyReleased
        typedString = tfSearch.getText().trim();
        searchResult.clear();
        
        System.out.println(tfSearch.getText().trim());
        System.out.println(typedString);
        
        if (0 != typedString.length()) {
            for (Genre genre : allGenres) {
                if (genre.getName().toLowerCase().startsWith(typedString.toLowerCase())) {
                    searchResult.add(genre);
                }
            }
            loadAllGenresModel(searchResult);
        }
        else{
            loadAllGenresModel(allGenres);
        }
    }//GEN-LAST:event_tfSearchKeyReleased


