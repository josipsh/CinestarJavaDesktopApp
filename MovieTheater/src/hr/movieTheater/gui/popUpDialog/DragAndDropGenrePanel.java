    private void init() {
        initLogHandler();
        initRepositiry();
        initData();
        initModels();
        initDragAndDrop();
        searchResult = new TreeSet<>();
    }

    private void initLogHandler() {
        logHandler = LogHandlerSingleton.getInstance();
    }

    private void initRepositiry() {
        repo = RepositorySingleton.getInstance();
    }

    private void initData() {
        try {
            allGenres = new TreeSet<>(repo.getGenres());
        } catch (SQLException ex) {
            logHandler.log(ex.getMessage(), ex);
            MessageUtils.showErrorMessage("Error br. 10", "Došlo je do pogreške tijekom dohvačanja podatka. Za pomoć obratite se adminu.");
            Logger.getLogger(DragAndDropGenrePanel.class.getName()).log(Level.SEVERE, null, ex);
        }catch (Exception ex) {
            logHandler.log(ex.getMessage(), ex);
            MessageUtils.showErrorMessage("Error br. 100", "Došlo je do nepoznate pogreške. Javite se adminu.");
        }
    }

    private void initModels() {
        genresModel = new DefaultListModel<>();
        allGenresModel = new DefaultListModel<>();
        loadGenresModel(genres);
        loadAllGenresModel(allGenres);
    }

    private void loadGenresModel(Set<Genre> z) {
        genresModel.clear();
        z.forEach(g -> genresModel.addElement(g));
        lsGenres.setModel(genresModel);
    }

    private void loadAllGenresModel(Set<Genre> z) {
        allGenresModel.clear();
        System.out.println("proslo");
        z.forEach(g -> allGenresModel.addElement(g));
        lsAllGenres.setModel(allGenresModel);
    }

    private void initDragAndDrop() {
        lsAllGenres.setDragEnabled(true);
        lsAllGenres.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        lsAllGenres.setTransferHandler(new ExsportHandler());
        
        lsGenres.setDropMode(DropMode.ON);
        lsGenres.setTransferHandler(new ImoportHandler());
    }
    
    
    private class ExsportHandler extends TransferHandler{

        @Override
        public int getSourceActions(JComponent c) {
            return COPY;
        }
        
        @Override
        protected Transferable createTransferable(JComponent c) {
            return new GenreTransferable(lsAllGenres.getSelectedValue());
        }
        
    }
    
    private class ImoportHandler extends TransferHandler{

        @Override
        public boolean canImport(TransferSupport support) {
            return support.getDataFlavors().equals(GenreTransferable.SUPPORTED_FLAVORS);
        }

        @Override
        public boolean importData(TransferSupport support) {
            boolean ok = false;
            if (support.isDataFlavorSupported(GenreTransferable.GENRE_FLAVOR)) {
                Transferable data = support.getTransferable();
                try {
                    genres.add( (Genre) data.getTransferData(GenreTransferable.GENRE_FLAVOR));
                    loadGenresModel(genres);
                } catch (UnsupportedFlavorException | IOException ex) {
                    logHandler.log(ex.getMessage(), ex);
                    MessageUtils.showErrorMessage("Error br. 30", "Došlo je do pogreške tijekom dropanja. Za pomoc obratite se adminu.");
                    Logger.getLogger(DragAndDropGenrePanel.class.getName()).log(Level.SEVERE, null, ex);
                }
                ok = true;
            }
            return ok;
        }
        
    }
