    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (isFormOk()) {
            
            try {
                user.get().setPasword(String.valueOf(tfNewPasword.getText().trim().hashCode()));
                repo.updateUser(user.get());
                dispose();
            } catch (SQLException ex) {
                logHandler.log(ex.getMessage(), ex);
                MessageUtils.showErrorMessage("Error br 10", "Došlo je do pogreške tijekom spremanja podataka od korisnika. Za pomoć obratite se razvojnom timu");
                Logger.getLogger(CreateUser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidUserTypeException ex) {
                logHandler.log(ex.getMessage(), ex);
                MessageUtils.showErrorMessage("Error br. 60", "Došlo je do pogreške tijekom spremanja podataka od korisnika. Za pomoć obratite se razvojnom timu");
                Logger.getLogger(CreateUser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                logHandler.log(ex.getMessage(), ex);
                MessageUtils.showErrorMessage("Error br. 100", "Došlo je do nepoznate pogreške. Javite se adminu.");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed


