    private boolean isFormOk(){
        boolean ok = true;
        if (tfNewPasword.getText().trim().isEmpty() || tfNewPaswordRepeted.getText().trim().isEmpty()) {
            ok = false;
        }
        
        if (!tfNewPasword.getText().trim().equals(tfNewPaswordRepeted.getText().trim())) {
            ok = false;
            MessageUtils.showInformationMessage("Korisnička greška", "Lozinke se ne podudaraju!");
        }
        
        return ok;
    }
